package cn.edu.zucc.zql.bean;

public class HomeItem {
	private String fileID;
	private String fileName;
	private String fileType;
	private String fileDesc;
	private String fileClassID;
	private String fileSupportCount;
	private String fileDownloadCount;
	private String fileUploader;
	private String fileUploadTime;
	public String getFileID() {
		return fileID;
	}
	public void setFileID(String fileID) {
		this.fileID = fileID;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFileType() {
		return fileType;
	}
	public void setFileType(String fileType) {
		this.fileType = fileType;
	}
	public String getFileDesc() {
		return fileDesc;
	}
	public void setFileDesc(String fileDesc) {
		this.fileDesc = fileDesc;
	}
	public String getFileSupportCount() {
		return fileSupportCount;
	}
	public void setFileSupportCount(String fileSupportCount) {
		this.fileSupportCount = fileSupportCount;
	}
	public String getFileDownloadCount() {
		return fileDownloadCount;
	}
	public void setFileDownloadCount(String fileDownloadCount) {
		this.fileDownloadCount = fileDownloadCount;
	}
	public String getFileUploader() {
		return fileUploader;
	}
	public void setFileUploader(String fileUploader) {
		this.fileUploader = fileUploader;
	}
	public String getFileUploadTime() {
		return fileUploadTime;
	}
	public void setFileUploadTime(String fileUploadTime) {
		this.fileUploadTime = fileUploadTime;
	}
	public String getFileClassID() {
		return fileClassID;
	}
	public void setFileClassID(String fileClassID) {
		this.fileClassID = fileClassID;
	}
}
